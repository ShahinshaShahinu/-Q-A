// While Loop :- Checks the condition before executing the block of code. 
//Do-while :- Executes the block of code once, then checks the condition.
//  It guarantees/sure at least one execution of the block.


let count =0;
while (count<5) {
    console.log('count is -',count);
    count ++
}

// Example of a do-while loop

let number = 0;
do {
  console.log("Number is: " + number);
  number++
} while (number < 5);
