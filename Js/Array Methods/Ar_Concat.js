// The concat() method - 
// creates a new array by merging (concatenating) existing arrays:

const myGirls = ["Cecilie", "Lone"];
const myBoys = ["Emil", "Tobias", "Linus"];

const NewAR=myBoys.concat(myGirls);
console.log(NewAR );

