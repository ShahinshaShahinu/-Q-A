// The shift() method-
//  removes the first array element 
// and "shifts" all other elements to a lower index. 

const fruits = ["Banana", "Orange", "Apple", "Mango"];
fruits.shift();
console.log(fruits)

// we will get the shifted elments , 
const Secondfruits = ["Banana", "Orange", "Apple", "Mango"];
console.log(Secondfruits.shift())
